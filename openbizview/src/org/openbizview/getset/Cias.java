package org.openbizview.getset;

public class Cias {
  private String vcodcia;
  private String vdescia;
/**
 * @return the vcodcia
 */
public String getVcodcia() {
	return vcodcia;
}
/**
 * @param vcodcia the vcodcia to set
 */
public void setVcodcia(String vcodcia) {
	this.vcodcia = vcodcia;
}
/**
 * @return the vdescia
 */
public String getVdescia() {
	return vdescia;
}
/**
 * @param vdescia the vdescia to set
 */
public void setVdescia(String vdescia) {
	this.vdescia = vdescia;
}
  
  
}
