package org.openbizview.getset;

public class Dimensiones {
	private String vcoddim;
	private String vdesdim;
	/**
	 * @return the vcoddim
	 */
	public String getVcoddim() {
		return vcoddim;
	}
	/**
	 * @param vcoddim the vcoddim to set
	 */
	public void setVcoddim(String vcoddim) {
		this.vcoddim = vcoddim;
	}
	/**
	 * @return the vdesdim
	 */
	public String getVdesdim() {
		return vdesdim;
	}
	/**
	 * @param vdesdim the vdesdim to set
	 */
	public void setVdesdim(String vdesdim) {
		this.vdesdim = vdesdim;
	}
	
	

}
