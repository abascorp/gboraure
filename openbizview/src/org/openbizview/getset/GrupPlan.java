public String getVnomen() {
	return vnomen;
}
/**
 * @param vnomen the vnomen to set
 */
public void setVnomen(String vnomen) {
	this.vnomen = vnomen;
}
/**
 * @return the vord
 */
public String getVord() {
	return vord;
}
/**
 * @param vord the vord to set
 */
